    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTextField3.getText().equals("")){
            jTextField3.requestFocus();
            JOptionPane.showMessageDialog(null,"O campo Nome √© obrigat√≥rio","Aviso",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(dataNascimento.getText().equals("")){
            dataNascimento.requestFocus();
            JOptionPane.showMessageDialog(null,"O campo Data de Nascimento √© obrigat√≥rio","Aviso",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try{
        ProgressBar xProgress = new ProgressBar();
        xProgress.setLocationRelativeTo(null);
        xProgress.setVisible(true);
        
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int num = 1; num<=100; num++) {
                    try {
                        xProgress.jp_progress.UpdateProgress(num);
                        xProgress.jp_progress.repaint();
                        Thread.sleep(7);
                        jTextField3.setText("");
                        dataNascimento.setText("");
                    } catch (InterruptedException ex) {
                        java.util.logging.Logger.getLogger(RegistEnt.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                    }
                }
            }
        }).start();
        }catch(Exception err){
            JOptionPane.showMessageDialog(null,"Ocorreu um erro","Aviso",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

